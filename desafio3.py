"""
    DADOS 2 NUMEROS, CALCULE A SOMA, SUBTRAÇÃO, DIVISÃO E MULTIPLICAÇÃO

    Ex:
    n1 = 20
    n1= 4
    soma = 24
    sub = 16
    div = 5
    mult = 80
"""

# obter numeros
n1 = float(input("insira um numero: "))
n2 = float(input("insira outro numero: "))

# calcular operações
soma = n1 + n2
sub = n1 - n2
div = n1 / n2
mult = n1 * n2

# exibir os resultados
print("\n")
print("soma: ", soma)
print("subtração: ", sub)
print("divisão: ", div)
print("multiplicação: ", mult)
print("\n")
# "\n" renderiza uma linha vazia
